package com.gustavo.exceptions;

/**
 *
 * @author Gustavo Cardenas Alba
 */
public class ConvertValueException extends Exception {
   
    /**
     *
     * @param message
     */
    public ConvertValueException(String message) {
        super(message);
    }
    
    @Override
    public String getMessage() {
        return super.getMessage();
    }
}
